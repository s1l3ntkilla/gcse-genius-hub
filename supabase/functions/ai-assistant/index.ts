import "https://deno.land/x/xhr@0.1.0/mod.ts";
import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages, difficulty } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get('LOVABLE_API_KEY');

    if (!LOVABLE_API_KEY) {
      throw new Error('LOVABLE_API_KEY is not configured');
    }

    // Adjust system prompt based on difficulty level
    let complexityInstruction = '';
    if (difficulty < 33) {
      complexityInstruction = 'Explain concepts in very simple terms, as if talking to a beginner. Use analogies and avoid technical jargon.';
    } else if (difficulty < 66) {
      complexityInstruction = 'Provide balanced explanations suitable for GCSE-level students. Include some technical terms but explain them.';
    } else {
      complexityInstruction = 'Provide detailed, advanced explanations. You can use technical terminology and go deeper into concepts.';
    }

    const systemPrompt = `You are a friendly AI Learning Assistant for GCSE students helping with Maths, Chemistry, Biology, Computer Science, French, and Music.

${complexityInstruction}

CRITICAL: Keep ALL responses SHORT and SUMMARIZED.
- Maximum 2-3 sentences for simple questions
- Maximum 4-5 bullet points for explanations
- Get straight to the point - no fluff or lengthy introductions
- Use bullet points over paragraphs
- Only expand if the student asks for more detail
- Be encouraging but brief`;

    const response = await fetch('https://ai.gateway.lovable.dev/v1/chat/completions', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${LOVABLE_API_KEY}`,
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        model: 'google/gemini-2.5-flash',
        messages: [
          { role: 'system', content: systemPrompt },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: 'Rate limit exceeded. Please try again in a moment.' }), {
          status: 429,
          headers: { ...corsHeaders, 'Content-Type': 'application/json' },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: 'AI credits exhausted. Please try again later.' }), {
          status: 402,
          headers: { ...corsHeaders, 'Content-Type': 'application/json' },
        });
      }
      const errorText = await response.text();
      console.error('AI gateway error:', response.status, errorText);
      throw new Error(`AI gateway error: ${response.status}`);
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, 'Content-Type': 'text/event-stream' },
    });
  } catch (error) {
    console.error('AI Assistant error:', error);
    return new Response(JSON.stringify({ error: error instanceof Error ? error.message : 'Unknown error' }), {
      status: 500,
      headers: { ...corsHeaders, 'Content-Type': 'application/json' },
    });
  }
});
